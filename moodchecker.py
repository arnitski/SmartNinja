mood = input("how do you feel?")
if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up, mate!")
elif mood == "excited":
    print("that's great!")
elif mood == "relaxed":
    print("relaxing is very important to stay healthy")
else:
    print("I don't recognize this mood")
